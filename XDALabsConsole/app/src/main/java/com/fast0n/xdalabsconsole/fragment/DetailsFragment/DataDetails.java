package com.fast0n.xdalabsconsole.fragment.DetailsFragment;

public class DataDetails {

    public final String id;
    final String title;
    final String type;
    final String value;
    final String alert;


    DataDetails(String id, String title, String type, String value, String alert) {
        this.id = id;
        this.title = title;
        this.type = type;
        this.value = value;
        this.alert = alert;
    }

}
